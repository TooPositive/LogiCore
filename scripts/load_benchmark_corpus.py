"""Load generated benchmark corpus from JSON file.

Reads diverse documents generated by an LLM (see corpus_generation_prompt.md)
and merges them with the original 12-doc corpus for re-ranking benchmarks.

Usage:
    # Validate corpus
    python scripts/load_benchmark_corpus.py

    # Used by benchmark_reranking_v2.py
    from scripts.load_benchmark_corpus import load_diverse_corpus
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

CORPUS_PATH = Path(__file__).resolve().parent.parent / "data" / "benchmark-corpus" / "diverse_docs.json"

REQUIRED_FIELDS = {"doc_id", "title", "doc_type", "department", "clearance_level", "text"}
VALID_DOC_TYPES = {
    "safety_manual", "hr_policy", "technical_spec", "incident_report",
    "meeting_minutes", "sop", "training_material", "compliance_audit",
    "vendor_agreement",
}
VALID_DEPARTMENTS = {"warehouse", "hr", "logistics", "management", "legal", "it"}


def load_diverse_corpus(path: Path | None = None) -> list[dict]:
    """Load generated diverse corpus from JSON file.

    Returns list of dicts with keys: doc_id, text, department, clearance.
    """
    corpus_path = path or CORPUS_PATH
    if not corpus_path.exists():
        raise FileNotFoundError(
            f"Corpus file not found: {corpus_path}\n"
            "Generate it using the prompt in scripts/corpus_generation_prompt.md\n"
            "and save the JSON output to data/benchmark-corpus/diverse_docs.json"
        )

    with open(corpus_path) as f:
        raw = json.load(f)

    docs = []
    for entry in raw:
        docs.append({
            "doc_id": entry["doc_id"],
            "text": entry["text"],
            "department": entry["department"],
            "clearance": entry.get("clearance_level", 1),
        })

    return docs


def validate_corpus(path: Path | None = None) -> None:
    """Validate the generated corpus for completeness and quality."""
    corpus_path = path or CORPUS_PATH

    with open(corpus_path) as f:
        docs = json.load(f)

    print(f"Loaded {len(docs)} documents from {corpus_path.name}")

    errors = []
    warnings = []
    doc_types: dict[str, int] = {}
    lengths: list[int] = []

    for i, doc in enumerate(docs):
        # Check required fields
        missing = REQUIRED_FIELDS - set(doc.keys())
        if missing:
            errors.append(f"Doc {i}: missing fields {missing}")
            continue

        # Check doc_type
        if doc["doc_type"] not in VALID_DOC_TYPES:
            errors.append(f"Doc {i} ({doc['doc_id']}): invalid doc_type '{doc['doc_type']}'")

        # Check department
        if doc["department"] not in VALID_DEPARTMENTS:
            errors.append(f"Doc {i} ({doc['doc_id']}): invalid department '{doc['department']}'")

        # Check clearance level
        if not 1 <= doc.get("clearance_level", 1) <= 4:
            errors.append(f"Doc {i} ({doc['doc_id']}): clearance_level must be 1-4")

        # Check text length
        text_len = len(doc["text"])
        lengths.append(text_len)
        if text_len < 200:
            warnings.append(f"Doc {i} ({doc['doc_id']}): very short ({text_len} chars)")
        if text_len > 3000:
            warnings.append(f"Doc {i} ({doc['doc_id']}): very long ({text_len} chars)")

        # Count doc types
        dt = doc["doc_type"]
        doc_types[dt] = doc_types.get(dt, 0) + 1

    # Check uniqueness
    ids = [d["doc_id"] for d in docs]
    if len(ids) != len(set(ids)):
        dupes = [x for x in ids if ids.count(x) > 1]
        errors.append(f"Duplicate doc_ids: {set(dupes)}")

    # Print report
    print(f"\nDoc type distribution:")
    for dt, count in sorted(doc_types.items()):
        print(f"  {dt:<20} {count}")

    if lengths:
        print(f"\nText lengths: min={min(lengths)}, max={max(lengths)}, avg={sum(lengths)//len(lengths)}")

    if errors:
        print(f"\nERRORS ({len(errors)}):")
        for e in errors:
            print(f"  {e}")
    else:
        print(f"\nNo errors.")

    if warnings:
        print(f"\nWarnings ({len(warnings)}):")
        for w in warnings:
            print(f"  {w}")

    if errors:
        sys.exit(1)
    else:
        print(f"\nCorpus is valid. Ready for benchmark.")


if __name__ == "__main__":
    validate_corpus()
